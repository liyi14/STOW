class Transformer(nn.Module):

    def __init__(self, d_model=512, nhead=8, num_encoder_layers=6, dim_feedforward=2048, dropout=0.1,
                 activation="relu", normalize_before=False, layer_structure=None):
        super().__init__()

        encoder_layer = TransformerEncoderLayer(d_model, nhead, dim_feedforward,
                                                dropout, activation, normalize_before)
        encoder_norm = nn.LayerNorm(d_model) if normalize_before else None
        self.encoder = TransformerEncoder(encoder_layer, num_encoder_layers, encoder_norm, layer_structure)

        self._reset_parameters()

        self.d_model = d_model
        self.nhead = nhead
        self.d_feed = dim_feedforward
        # self.scale_factor = float(d_model // nhead) ** 0.5

    def _reset_parameters(self):
        for p in self.parameters():
            if p.dim() > 1:
                nn.init.xavier_uniform_(p)

    def forward(self, feat, mask=None, pos_embed=None, return_intermediate=False):
        """

        :param feat: (H1W1+H2W2, bs, C)
        :param mask: (bs, H1W1+H2W2)
        :param pos_embed: (H1W1+H2W2, bs, C)
        :param return_intermediate: whether to return intermediate layers' feature maps
        :return:
        """
        memory = self.encoder(feat, src_key_padding_mask=mask, pos=pos_embed, return_intermediate=return_intermediate)
        return memory


def _get_clones(module, N):
    return nn.ModuleList([copy.deepcopy(module) for i in range(N)])


def build_transformer_encoder(d_model=256, dropout=0.1, nhead=8, dim_feedforward=2048, num_encoder_layers=1, normalize_before=False, layer_structure=['full'], activation='relu'):

    encoder_class = Transformer
    encoder = encoder_class(
        d_model=d_model, dropout=dropout, nhead=nhead,
        dim_feedforward=dim_feedforward, num_encoder_layers=num_encoder_layers,
        normalize_before=normalize_before, layer_structure=layer_structure,
        activation=activation
    )
    return encoder


def _get_activation_fn(activation):
    """Return an activation function given a string"""
    if activation == "relu":
        return F.relu
    if activation == "gelu":
        return F.gelu
    if activation == "glu":
        return F.glu
    raise RuntimeError(F"activation should be relu/gelu, not {activation}.")
