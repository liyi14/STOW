import torch.nn.functional as F
from typing import Optional
from torch import nn, Tensor
from stow.modeling.transformer_decoder.frame_transformer import TransformerEncoderLayer

class TransformerEncoder(nn.Module):

    def __init__(self, encoder_layer, num_layers, norm=None, layer_structure=None):
        super().__init__()
        self.layers = _get_clones(encoder_layer, num_layers)
        self.num_layers = num_layers
        self.norm = norm
        if layer_structure is None:
            self.layer_structure = ['full'] * num_layers
        else:
            assert  len(layer_structure) == self.num_layers
            self.layer_structure = layer_structure

    def forward(self, src,
                mask: Optional[Tensor] = None,
                src_key_padding_mask: Optional[Tensor] = None,
                pos: Optional[Tensor] = None,
                return_intermediate=False):
        if return_intermediate:
            raise NotImplemented
            output_list = []
            output = src

            for layer in self.layers:
                output = layer(output, src_mask=mask,
                               src_key_padding_mask=src_key_padding_mask, pos=pos)
                if self.norm is None:
                    output_list.append(output)
            if self.norm is not None:
                output = self.norm(output)
                output_list.append(output)
            return output_list
        else:
            output = src
            for layer_type, layer in zip(self.layer_structure, self.layers):
                if layer_type == 'full':
                    output = layer(output, src_mask=mask,
                                src_key_padding_mask=src_key_padding_mask, pos=pos)
                elif layer_type == 'frame':
                    output_list = []
                    q = 100
                    num_frame = src.shape[0]//q
                    for f in range(num_frame):
                        src_mask_f = mask[f*q:(f+1)*q] if mask is not None else None 
                        src_key_padding_mask_f = src_key_padding_mask[f*q:(f+1)*q] if src_key_padding_mask is not None else None
                        pos_f = pos[f*q:(f+1)*q] if pos is not None else None
                        output_f = layer(output[f*q:(f+1)*q], src_mask=src_mask_f,
                                        src_key_padding_mask=src_key_padding_mask_f, pos=pos_f)
                        output_list.append(output_f)
                    
                    output = torch.cat(output_list, dim=0)
                else:
                    raise KeyError(f'layer_type {layer_type} not supported')

            if self.norm is not None:
                output = self.norm(output)

            return output


